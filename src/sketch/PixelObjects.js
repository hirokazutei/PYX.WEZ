function PixelNumber(x, y, n) {
  this.x = x;
  this.y = y;
  this.n = n;
  if (n == ":") {
    this.n = 12;
  } else if (n == "o") {
    this.n = 10;
  } else if (n == "C") {
    this.n = 11;
  } else if (n == "-") {
    this.n = 13;
  }
  fill(100);
  for (var i = 0; i < pixelnumbers[this.n][0].length; i++) {
    rect(this.x + pixelnumbers[this.n][0][i], this.y + pixelnumbers[this.n][1][i], 10, 10);
  }
}


